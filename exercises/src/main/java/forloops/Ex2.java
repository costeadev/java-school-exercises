package for_loops;

/*
 * Programa que muestre por pantalla 
 * todos los n√∫meros naturales del 1 hasta el 5
 * ambos incluidos; 
 * Luego lo mismo pero de 1 a 50;
 * Luego lo mismo pero de 1 a 500.  
 */

public class Ex2 {
  public static void main(String[] args) {
	  // 1 al 5
	  for(int i = 1; i <= 5; i++) {
		  System.out.println(i);
	  }
	  // 1 al 50
	  for(int i = 1; i <= 50; i++) {
		  System.out.println(i);
	  }
	  // 1 al 500
	  for(int i = 1; i <= 500; i++) {
		  System.out.println(i);
	  }
  }
}
